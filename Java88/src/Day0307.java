

public class Day0307 {
	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		a += b;	//a = a + b;
		System.out.println(a);
		a -= b;	//a = a - b;
		System.out.println(a);
		a *= b; //a = a * b;
		System.out.println(a);
		
		a = 10;
		System.out.println(a);
		a *= 3 + 10;
		System.out.println(a);
		
		a += 1;
		System.out.println(a);
		a += 1;
		System.out.println(a);
	}
}
