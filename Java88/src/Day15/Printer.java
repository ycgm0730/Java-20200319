package Day15;

public class Printer {
	static	void println(int arg) {
		System.out.println(arg);
	}
	static	void println(boolean arg) {
		System.out.println(arg);
	}
	static	void println(double arg) {
		System.out.println(arg);
	}
	static	void println(String arg) {
		System.out.println(arg);
	}
}
